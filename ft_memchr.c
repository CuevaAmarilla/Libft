#include "libft.h"
#include <string.h>

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t		i;
	const char	*str;

	i = 0;
	str = (const char *)s;
	while (n)
	{
		if (str[i] == c)
			return ((void *)&s[i]);
		i++;
		n--;
	}
	return (NULL);
}
